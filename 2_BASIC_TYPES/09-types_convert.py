# Funciones nativas de Python
# int()
# str() TransString
# float() TransFloat
# bool() TransBool con los conceptos de Truthy(True) y Falsy(False)
# Datos Falsy, serian un string vacio, cero y none

print(bool(""))  # Cadena vacia, es false
print(bool("0"))  # Cadena no vacia, es true
print(bool(None))  # Valor de vacio, es false
print(bool(" "))  # Cadena que contiene espacio, es true
print(bool(0))  # Valor cero, es false
