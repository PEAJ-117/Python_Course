# Importacion del modulo math, codigo python para utilizar sus funciones
import math

# La funcion round() declara el valor mas cercano
print(round(1.3))
print(round(1.7))
print(round(1.5))

# La funcion abs() convierte numeros a valores absolutos
print(abs(-77))
print(abs(55))

print(math.ceil(1.1))  # Lleva al numero superior mas cercano
print(math.floor(1.9999))  # Lleva al numero inferior mas cercano
print(math.isnan(23))  # Declara si el valor no es un numero
# print(math.isnan("23"))
print(math.pow(10, 3))  # Potencia (numero, potencia)
print(math.sqrt(9))  # Raiz cuadrada
